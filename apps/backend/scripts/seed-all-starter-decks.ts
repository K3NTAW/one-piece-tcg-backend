async function main() {
  console.log('🌱 Starting comprehensive One Piece TCG ALL starter deck cards seeding...');
  console.log(`📚 Adding ${ALL_STARTER_DECK_CARDS.length} starter deck cards`);

  let successCount = 0;
  let errorCount = 0;

  for (const cardData of ALL_STARTER_DECK_CARDS) {
    try {
      await prisma.card.upsert({
        where: { apiId: cardData.apiId },
        update: cardData,
        create: cardData,
      });
      console.log(`✅ Seeded starter deck card: ${cardData.name} (${cardData.rarity}) from ${cardData.setCode}`);
      successCount++;
    } catch (error: any) {
      console.error(`❌ Error seeding starter deck card ${cardData.name} from ${cardData.setCode}:`, error.message);
      errorCount++;
    }
  }

  console.log(`\n🎉 Complete starter deck seeding completed!`);
  console.log(`✅ Successfully seeded: ${successCount} cards`);
  console.log(`❌ Total errors: ${errorCount} cards`);
}

